private void loadAllRecords() {
    try {
        String query = "SELECT * FROM doctors"; // Table: doctors
        ps = con.prepareStatement(query);
        rs = ps.executeQuery();

        ids.clear(); // Clear the list of IDs
        while (rs.next()) {
            ids.add(rs.getInt("DoctorID")); // Ensure "DoctorID" matches your database column
        }
        if (!ids.isEmpty()) {
            currentIndex = 0; // Start from the first record
            fetchRecord(ids.get(currentIndex));
        }
        end.setText(String.valueOf(ids.size())); // Update record count in the UI
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(rootPane, "Error loading records: " + e.getMessage());
    }
}

private void fetchRecord(int doctorId) {
   try {
        String query = "SELECT * FROM doctors WHERE DoctorID = ?";
        ps = con.prepareStatement(query);
        ps.setInt(1, doctorId);
        rs = ps.executeQuery();

        if (rs.next()) {
            Doctor_id.setText(rs.getString("DoctorID"));
            Doctor_name.setText(rs.getString("fname"));
            Doctor_last.setText(rs.getString("lname"));
            Doctor_speci.setText(rs.getString("specialization"));
            contact.setText(rs.getString("contact_number"));
            doctor_clinic.setText(rs.getString("clinic"));
            full.setText(rs.getString("full_name"));
        } else {
            JOptionPane.showMessageDialog(rootPane, "Record not found for ID: " + doctorId);
        }
        start.setText(String.valueOf(currentIndex + 1)); // Update the current position
        end.setText(String.valueOf(ids.size())); // Update the total record count
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(rootPane, "Error fetching record: " + e.getMessage());
    }
}

private void insertRecord() {
    // Read input values from the UI fields
    String id = Doctor_id.getText();
    String fname = Doctor_name.getText();
    String lname = Doctor_last.getText();
    String specialization = Doctor_speci.getText();
     String cont = contact.getText();
    String clinic = doctor_clinic.getText();
    String fullN = full.getText();

    // Validate that all fields are filled
    if (id.equals("") || fname.equals("") || specialization.equals("") || clinic.equals("" )|| lname.equals("") ||cont.equals("")||full.equals("")) {
        JOptionPane.showMessageDialog(rootPane, "All fields are required!");
    } else {
        try {
            String sql = "INSERT INTO doctors (DoctorID, fname,lname ,specialization,contact_number ,clinic,full_name) VALUES (?, ?,?,?, ?, ?,?)";
            ps = con.prepareStatement(sql);
            ps.setString(1, id);
            ps.setString(2, fname);
            ps.setString(3, lname);
            ps.setString(4, specialization);
            ps.setString(5, cont);
            ps.setString(6, clinic);
            ps.setString(7, fullN);

            int rowsInserted = ps.executeUpdate(); // Execute the insert operation
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(rootPane, "Record inserted successfully!");
                loadAllRecords(); // Refresh the list of records
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Error inserting record: " + e.getMessage());
        }
    }
}
