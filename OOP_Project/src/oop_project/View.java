    try {
        // Query to fetch all doctor records by first name (fname)
        String query = "SELECT * FROM doctors WHERE fname = ?";
        ps = con.prepareStatement(query);
        ps.setString(1, inputName);
        rs = ps.executeQuery();

        ids.clear(); // Clear the previous list of IDs
        while (rs.next()) {
            ids.add(rs.getInt("DoctorID")); // Collect all matching DoctorIDs
        }

        if (!ids.isEmpty()) {
            currentIndex = 0; // Start from the first record
            fetchRecord(ids.get(currentIndex)); // Show the first matching record
        } else {
            JOptionPane.showMessageDialog(rootPane, "No records found for First Name: " + inputName);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(rootPane, "Error finding record: " + e.getMessage());
    }
